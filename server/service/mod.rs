use serde::{Deserialize, Serialize};

pub(crate) mod grpc;
pub mod http;
pub(crate) mod state;
mod transaction_service;

#[derive(Serialize, Deserialize, Debug, Clone, Copy, PartialOrd, PartialEq, Eq, Hash)]
#[serde(rename_all = "camelCase")]
pub enum TransactionType {
    Read,
    Write,
    Schema,
}

#[derive(Serialize, Deserialize, Debug, Clone, Copy, PartialOrd, PartialEq, Eq, Hash)]
#[serde(rename_all = "camelCase")]
pub enum QueryType {
    Read,
    Write,
    Schema,
}

#[derive(Serialize, Deserialize, Debug, Clone, Copy, PartialOrd, PartialEq, Eq, Hash)]
#[serde(rename_all = "camelCase")]
pub enum AnswerType {
    Ok,
    ConceptRows,
    ConceptDocuments,
}
